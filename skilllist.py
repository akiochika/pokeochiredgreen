def get_skill_damage(skill_name, attacker, defender):
    skill = skill_list.get(skill_name)
    if not skill:
        return 0  # 技が存在しない場合、ダメージは0

    base_damage = skill["damage"]
    attack_stat = attacker["attack"]
    defense_stat = defender["defense"]

    # シンプルなダメージ計算
    damage = ((2 * attacker["level"] / 5 + 2) * base_damage * attack_stat / defense_stat) / 50 + 2
    return int(damage)